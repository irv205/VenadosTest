package com.example.venadosdefinitivo.models.estadisticas;

public class Estadisticas {

    private int position;
    private String image;
    private String team;
    private int games;
    private int win;
    private int loss;
    private int tie;
    private int f_goals;
    private int a_goals;
    private int score_diff;
    private int points;
    private int efec;

    public int getPosition() {
        return position;
    }

    public void setPosition(int position) {
        this.position = position;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public int getGames() {
        return games;
    }

    public void setGames(int games) {
        this.games = games;
    }

    public int getWin() {
        return win;
    }

    public void setWin(int win) {
        this.win = win;
    }

    public int getLoss() {
        return loss;
    }

    public void setLoss(int loss) {
        this.loss = loss;
    }

    public int getTie() {
        return tie;
    }

    public void setTie(int tie) {
        this.tie = tie;
    }

    public int getF_goals() {
        return f_goals;
    }

    public void setF_goals(int f_goals) {
        this.f_goals = f_goals;
    }

    public int getA_goals() {
        return a_goals;
    }

    public void setA_goals(int a_goals) {
        this.a_goals = a_goals;
    }

    public int getScore_diff() {
        return score_diff;
    }

    public void setScore_diff(int score_diff) {
        this.score_diff = score_diff;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }

    public int getEfec() {
        return efec;
    }

    public void setEfec(int efec) {
        this.efec = efec;
    }
}
